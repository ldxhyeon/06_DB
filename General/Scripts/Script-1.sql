SELECT
	EMP_ID,
	EMP_NAME,
	DECODE (SUBSTR(EMP_NO, 8, 1), '1', 'M', '2', 'F') AS 성별,
	SALARY,
	JOB_NAME,
	NVL(DEPT_TITLE, '없음')
FROM
	EMPLOYEE
JOIN
	JOB USING(JOB_CODE)
LEFT JOIN
	DEPARTMENT ON(DEPT_ID = DEPT_CODE)
WHERE
	DECODE (SUBSTR(EMP_NO, 8, 1), '1', 'M', '2', 'F') = 'F'
	AND
	SALARY BETWEEN 3000000 AND 4000000
ORDER BY
	SALARY DESC;



