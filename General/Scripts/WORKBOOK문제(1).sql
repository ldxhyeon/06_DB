SELECT
	C.CLASS_NAME,
	P.PROFESSOR_NAME
FROM
	TB_CLASS C
JOIN
	TB_CLASS_PROFESSOR CP ON(C.CLASS_NO = CP.CLASS_NO)
JOIN
	TB_PROFESSOR P ON(P.PROFESSOR_NO = CP.PROFESSOR_NO)
JOIN
	-- USING은 3개부터 불가능
	TB_DEPARTMENT D ON(D.DEPARTMENT_NO = P.DEPARTMENT_NO)
WHERE 
	D.CATEGORY = '인문사회';
ORDER BY
	PROFESSOR_NAME ASC;



