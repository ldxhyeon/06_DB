-- 13번
SELECT
	-- null일 경우 ''공백으로 대체
	NVL(SUBSTR(TERM_NO,1,4), ' ') AS 년도,
	NVL(SUBSTR(TERM_NO,5,2),' ') AS 학기,
	ROUND(AVG(POINT),1) "평점"
FROM
	TB_GRADE
WHERE
	STUDENT_NO = 'A112113'
GROUP BY
	-- 공백 생성 하려면 ROLLUP
	ROLLUP(SUBSTR(TERM_NO,1,4),SUBSTR(TERM_NO,5,2))
ORDER BY
	SUBSTR(TERM_NO,1,4);



