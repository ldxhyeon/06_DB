-- 9번 문제
SELECT
	STUDENT_NO,
	STUDENT_NAME,
	STUDENT_SSN,
	STUDENT_ADDRESS
FROM 
	TB_STUDENT
WHERE 
	SUBSTR(STUDENT_ADDRESS, 1, 2) IN('전주') AND SUBSTR(STUDENT_NO, 1, 2) IN ('A2') AND ABSENCE_YN = 'N';



-- 9번 두번째 방법
