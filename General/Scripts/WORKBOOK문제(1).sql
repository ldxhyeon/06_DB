-- 2번 문제
-- 기본적으로 문자열 사용은 홑따옴표('')
SELECT 
	DEPARTMENT_NAME || '의 ' || '정원은 ' || CAPACITY || '명 입니다' "학과별 정원"
FROM 
	TB_DEPARTMENT;



-- 3번 문제
-- 기본적으로 문자열 사용은 홑따옴표('')
SELECT 
	STUDENT_NAME
FROM 
	TB_STUDENT
WHERE
  -- SUBSTR 문자열 추출 8번째 숫자가 2 나 4인 사람을 찾는 문행
	DEPARTMENT_NO = '001' AND SUBSTR(STUDENT_SSN, 8, 1) IN ('2', '4')
	-- 그리고 휴학중이면 Y인 사람
  AND ABSENCE_YN = 'Y';
 
 
 
 -- 4번 문제
